// Render the list of questlines and their completion states.
function renderPage() {
    if (characterData == null) {
        renderError("Character data could not be found. You can load a character from the <a class='alert-link' href='settings.html'>Settings</a>.")
        renderEmpty(categories)
        return
    }

    if (!achievementsPublicData) {
        renderError("The achievements for this character are not public. You can set Achievements to Public in your <a class='alert-link' href='https://na.finalfantasyxiv.com/lodestone/my/setting/account/'>character settings</a>.")
        renderEmpty(categories)
        return
    }

    for (let i = 0; i < achievementData["List"].length; i++) {
        achievements.set(achievementData["List"][i]["ID"], achievementData["List"][i]["Date"])
    }

    renderList("#main-story-quest", mainStoryQuest)
    renderList("#side-story-quests", sideStoryQuests)
    renderList("#duties-quests", dutiesQuests)
    renderList("#relic-quests", relicQuests)
    renderList("#beast-tribe-quests", beastTribeQuests)
    renderList("#allied-beast-tribe-quests", alliedBeastTribeQuests)
    renderList("#custom-delivery-quests", customDeliveryQuests)
    renderList("#combat-job-quests", combatJobQuests)
    renderList("#gatherer-job-quests", gathererJobQuests)
    renderList("#crafter-job-quests", crafterJobQuests)
    renderList("#role-quests", roleQuests)
}
